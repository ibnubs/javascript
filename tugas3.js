function perkalian(angka){
    return angka * 10;
}

var bilangan = perkalian(2);

console.log("nilai bilangan sebelum di ubah adalah = " + bilangan);

bilangan = perkalian(8);

console.log("nilai perkalian setelah di ubah ulang adalah = " + bilangan);

